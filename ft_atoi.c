#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t		i;
	long		res;
	size_t		posneg;

	i = 0;
	res = 0;
	posneg = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		posneg = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		res = res * 10;
		res = res + str[i] - '0';
		i++;
	}
	if (posneg == 1)
		return (-res);
	else
		return (res);
}
